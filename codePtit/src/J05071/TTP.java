public class TTP {
    private String ma, ten;
    private long gia;
    
    public TTP(String ma, String ten, long gia) {
        this.ma = ma;
        this.ten = ten;
        this.gia = gia;
    }
    
    public String getMa() {
        return ma;
    }
    
    public String getTen() {
        return ten;
    }
    
    public long getGia() {
        return gia;
    }
} 
